    private void llenarCombosFecha() {
        cmbDia.removeAllItems();
        cmbMes.removeAllItems();
        cmbAño.removeAllItems();
        for (int i = 1; i <= 31; i++) {
            cmbDia.addItem(String.format("%02d", i));
        }
        for (int i = 1; i <= 12; i++) {
            cmbMes.addItem(String.format("%02d", i));
        }
        for (int i = 2026; i <= 2035; i++) {
            cmbAño.addItem(String.valueOf(i));
        }
    }

    
    public void cargarLibros(List<Libro> libros) {
    DefaultComboBoxModel<Libro> modelo = new DefaultComboBoxModel<>();
    for (Libro libro : libros) {
        modelo.addElement(libro);
        }
    cmbLibro.setModel((javax.swing.ComboBoxModel) modelo);
    }

    public void cargarBibliotecarios(List<Bibliotecario> bibliotecarios) {
        DefaultComboBoxModel<Bibliotecario> modelo = new DefaultComboBoxModel<>();
        for (Bibliotecario bibliotecario : bibliotecarios) {
            modelo.addElement(bibliotecario);
        }
        cmbBibliotecario.setModel((javax.swing.ComboBoxModel) modelo);
    }
    
